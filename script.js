function generaFrase() {
    const randomIndex = Math.floor(Math.random() * frasi.length);
    return frasi[randomIndex];
}

document.addEventListener('DOMContentLoaded', () => {
    const appDiv = document.getElementById('app');

    const titolo = document.createElement('h1');
    titolo.innerText = 'Generatore di Frasi';
    appDiv.appendChild(titolo);

    const fraseElement = document.createElement('p');
    fraseElement.innerText = generaFrase();
    appDiv.appendChild(fraseElement);

    const bottoneGenera = document.createElement('button');
    bottoneGenera.innerText = 'Genera Frase';
    appDiv.appendChild(bottoneGenera);

    bottoneGenera.addEventListener('click', () => {
        fraseElement.innerText = generaFrase();
    });

    const bottoneCopia = document.createElement('button');
    bottoneCopia.innerText = 'Copia Frase';
    appDiv.appendChild(bottoneCopia);

    bottoneCopia.addEventListener('click', () => {
        navigator.clipboard.writeText(fraseElement.innerText).then(() => {
            alert('Frase copiata negli appunti!');
        });
    });
});